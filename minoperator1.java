//ternary operator -> conditional operator
// min between two number
class minoperator1
{
public static void main(String[] args)
{
int a=10, b=20;
int min=(a<b)?a:b;
int max=(a>b)?a:b;
System.out.println(a+ " " +b);
System.out.println(min);
System.out.println(min + " is smaller than " + max);
}
}